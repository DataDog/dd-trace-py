"""
Tests for per-span source:otel tag on evaluations.

source:otel is set only when the span dict carries is_otel=True, indicating the span
was generated by OTel gen.ai instrumentation (OTel semantic conventions). export_span()
sets is_otel via _span_api. The global DD_TRACE_OTEL_ENABLED flag is not used.
"""

import mock

from ddtrace.internal.constants import SPAN_API_DATADOG
from ddtrace.internal.constants import SPAN_API_OTEL


def test_submit_evaluation_source_otel_when_is_otel_true(llmobs, mock_llmobs_eval_metric_writer):
    """is_otel=True sets source:otel tag."""
    llmobs.submit_evaluation(
        span={"span_id": "123", "trace_id": "456", "is_otel": True},
        label="quality",
        metric_type="score",
        value=0.9,
        ml_app="test-app",
    )

    mock_llmobs_eval_metric_writer.enqueue.assert_called_once()
    call_args = mock_llmobs_eval_metric_writer.enqueue.call_args[0][0]
    assert "source:otel" in call_args["tags"]


def test_submit_evaluation_no_source_otel_when_is_otel_false(llmobs, mock_llmobs_eval_metric_writer):
    """is_otel=False suppresses source:otel, regardless of global DD_TRACE_OTEL_ENABLED."""
    with mock.patch("ddtrace.llmobs._llmobs.config._otel_trace_enabled", True):
        llmobs.submit_evaluation(
            span={"span_id": "123", "trace_id": "456", "is_otel": False},
            label="quality",
            metric_type="score",
            value=0.9,
            ml_app="test-app",
        )

    mock_llmobs_eval_metric_writer.enqueue.assert_called_once()
    call_args = mock_llmobs_eval_metric_writer.enqueue.call_args[0][0]
    assert "source:otel" not in call_args["tags"]


def test_submit_evaluation_no_source_otel_when_is_otel_missing(llmobs, mock_llmobs_eval_metric_writer):
    """Omitting is_otel does not set source:otel — users must set it explicitly."""
    llmobs.submit_evaluation(
        span={"span_id": "123", "trace_id": "456"},
        label="quality",
        metric_type="score",
        value=0.9,
        ml_app="test-app",
    )

    mock_llmobs_eval_metric_writer.enqueue.assert_called_once()
    call_args = mock_llmobs_eval_metric_writer.enqueue.call_args[0][0]
    assert "source:otel" not in call_args["tags"]


def test_export_span_sets_is_otel_false_for_ddtrace_span(llmobs):
    """export_span() sets is_otel=False for spans created via the ddtrace LLMObs API."""
    with llmobs.llm(model_name="test-model", name="test-span", model_provider="test-provider") as span:
        assert span._span_api == SPAN_API_DATADOG
        exported = llmobs.export_span(span=span)

    assert exported is not None
    assert exported["is_otel"] is False


def test_export_span_sets_is_otel_true_for_otel_api_span(llmobs):
    """export_span() sets is_otel=True for spans created via the OTel API."""
    with llmobs.llm(model_name="test-model", name="test-span", model_provider="test-provider") as span:
        span._span_api = SPAN_API_OTEL
        exported = llmobs.export_span(span=span)

    assert exported is not None
    assert exported["is_otel"] is True
